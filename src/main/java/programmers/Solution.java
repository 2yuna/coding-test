package programmers;

class Solution {

    public int solution(int width, int height, int[][] diagonals) {
        int[][] array = new int[width][height];
        for (int i = 0; i < width; i++) {
            for (int j = 0; j < height; j++) {
                array[i][j] = 2;

            }
        }

        return 0;
    }
}